def pruebaFuncion(nombre, apellido):  # lo que se encierra entre paréntesis es un parámetro
    print("¡Hola desde una función!")
    print(f"Bienvenido {nombre} {apellido}")


# lo que se encierra entre paréntesis es un argumento
pruebaFuncion("Kenny", "Mendo")
pruebaFuncion("Jacqueline", "Godoy")
